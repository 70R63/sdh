package de.hybris.sdh.core.pojos.responses;

/**
 * @author Consultor
 *
 */
public class ReteICA
{
	private String numObjeto;
	private String consecutive;
	private String numID;

	/**
	 * @return the numID
	 */
	public String getNumID()
	{
		return numID;
	}

	/**
	 * @param numID
	 *           the numID to set
	 */
	public void setNumID(final String numID)
	{
		this.numID = numID;
	}

	/**
	 * @return the numObjeto
	 */
	public String getNumObjeto()
	{
		return numObjeto;
	}

	/**
	 * @param numObjeto
	 *           the numObjeto to set
	 */
	public void setNumObjeto(final String numObjeto)
	{
		this.numObjeto = numObjeto;
	}

	/**
	 * @return the consecutive
	 */
	public String getConsecutive()
	{
		return consecutive;
	}

	/**
	 * @param consecutive
	 *           the consecutive to set
	 */
	public void setConsecutive(final String consecutive)
	{
		this.consecutive = consecutive;
	}



}
