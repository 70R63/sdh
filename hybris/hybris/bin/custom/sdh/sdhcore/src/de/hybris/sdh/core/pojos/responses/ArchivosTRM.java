package de.hybris.sdh.core.pojos.responses;

/**
 * @author hybris
 *
 */
public class ArchivosTRM
{
	private String fechaCarga;

	private String nomArchivo;

	private String perRepor;

	private String estado;

	private String numForm;

	private String bandera;

	/**
	 * @return the fechaCarga
	 */
	public String getFechaCarga()
	{
		return fechaCarga;
	}

	/**
	 * @param fechaCarga
	 *           the fechaCarga to set
	 */
	public void setFechaCarga(final String fechaCarga)
	{
		this.fechaCarga = fechaCarga;
	}

	/**
	 * @return the nomArchivo
	 */
	public String getNomArchivo()
	{
		return nomArchivo;
	}

	/**
	 * @param nomArchivo
	 *           the nomArchivo to set
	 */
	public void setNomArchivo(final String nomArchivo)
	{
		this.nomArchivo = nomArchivo;
	}

	/**
	 * @return the perRepor
	 */
	public String getPerRepor()
	{
		return perRepor;
	}

	/**
	 * @param perRepor
	 *           the perRepor to set
	 */
	public void setPerRepor(final String perRepor)
	{
		this.perRepor = perRepor;
	}

	/**
	 * @return the estado
	 */
	public String getEstado()
	{
		return estado;
	}

	/**
	 * @param estado
	 *           the estado to set
	 */
	public void setEstado(final String estado)
	{
		this.estado = estado;
	}

	/**
	 * @return the numForm
	 */
	public String getNumForm()
	{
		return numForm;
	}

	/**
	 * @param numForm
	 *           the numForm to set
	 */
	public void setNumForm(final String numForm)
	{
		this.numForm = numForm;
	}

	/**
	 * @return the bandera
	 */
	public String getBandera()
	{
		return bandera;
	}

	/**
	 * @param bandera
	 *           the bandera to set
	 */
	public void setBandera(final String bandera)
	{
		this.bandera = bandera;
	}






}
