package de.hybris.sdh.core.pojos.responses;

public class ObligacionesPublicidad {
	private String anoGravable;
	private String numResolucion;
	private String direccionValla;
	private String orientacionValla;
	private String estadoObligacion;
	/**
	 * @return the anoGravable
	 */
	public String getAnoGravable()
	{
		return anoGravable;
	}
	
	/**
	 * @param anoGravable
	 *           the anoGravable to set
	 */
	public void setAnoGravable(final String anoGravable)
	{
		this.anoGravable = anoGravable;
	}
	
	/**
	 * @return the numResolucion
	 */
	public String getNumResolucion()
	{
		return numResolucion;
	}

	/**
	 * @param numResolucion
	 *           the numResolucion to set
	 */
	public void setNumResolucion(final String numResolucion)
	{
		this.numResolucion = numResolucion;
	}

	/**
	 * @return the direccionValla
	 */
	public String getDireccionValla()
	{
		return direccionValla;
	}

	/**
	 * @param direccionValla
	 *           the direccionValla to set
	 */
	public void setDireccionValla(final String direccionValla)
	{
		this.direccionValla = direccionValla;
	}

	/**
	 * @return the orientacionValla
	 */
	public String getOrientacionValla()
	{
		return orientacionValla;
	}
	
	/**
	 * @param orientacionValla
	 *           the orientacionValla to set
	 */
	public void setOrientacionValla(final String orientacionValla)
	{
		this.orientacionValla = orientacionValla;
	}
	/**
	 * @return the estadoObligacion
	 */
	public String getEstadoObligacion()
	{
		return estadoObligacion;
	}
	
	/**
	 * @param estadoObligacion
	 *           the estadoObligacion to set
	 */
	public void setEstadoObligacion(final String estadoObligacion)
	{
		this.estadoObligacion = estadoObligacion;
	}

}
