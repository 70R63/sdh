public class EdoCtaDeliDetail
{
	private String radicado;
	private String anioGravable;
	private String estado;
	private String saldoCargo;
	private String saldoFavor;

	/**
	 * @return the radicado
	 */
	public String getRadicado()
	{
		return radicado;
	}

	/**
	 * @param radicado
	 *           the radicado to set
	 */
	public void setRadicado(final String radicado)
	{
		this.radicado = radicado;
	}

	/**
	 * @return the anioGravable
	 */
	public String getAnioGravable()
	{
		return anioGravable;
	}

	/**
	 * @param anioGravable
	 *           the anioGravable to set
	 */
	public void setAnioGravable(final String anioGravable)
	{
		this.anioGravable = anioGravable;
	}

	/**
	 * @return the estado
	 */
	public String getEstado()
	{
		return estado;
	}

	/**
	 * @param estado
	 *           the estado to set
	 */
	public void setEstado(final String estado)
	{
		this.estado = estado;
	}

	/**
	 * @return the saldoCargo
	 */
	public String getSaldoCargo()
	{
		return saldoCargo;
	}

	/**
	 * @param saldoCargo
	 *           the saldoCargo to set
	 */
	public void setSaldoCargo(final String saldoCargo)
	{
		this.saldoCargo = saldoCargo;
	}

	/**
	 * @return the saldoFavor
	 */
	public String getSaldoFavor()
	{
		return saldoFavor;
	}

	/**
	 * @param saldoFavor
	 *           the saldoFavor to set
	 */
	public void setSaldoFavor(final String saldoFavor)
	{
		this.saldoFavor = saldoFavor;
	}


}
