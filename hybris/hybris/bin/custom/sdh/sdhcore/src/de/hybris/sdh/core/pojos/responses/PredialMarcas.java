package de.hybris.sdh.core.pojos.responses;

/**
 * @author Federico Flores Dimas
 *
 */
public class PredialMarcas
{
	private String marca;
	private String tipoMarca;
	private String porcMarca;
	private String valorExencion;
	private String conservacionHistorica;

	/**
	 * @return the marca
	 */
	public String getMarca()
	{
		return marca;
	}

	/**
	 * @param marca
	 *           the marca to set
	 */
	public void setMarca(final String marca)
	{
		this.marca = marca;
	}

	/**
	 * @return the tipoMarca
	 */
	public String getTipoMarca()
	{
		return tipoMarca;
	}

	/**
	 * @param tipoMarca
	 *           the tipoMarca to set
	 */
	public void setTipoMarca(final String tipoMarca)
	{
		this.tipoMarca = tipoMarca;
	}

	/**
	 * @return the porcMarca
	 */
	public String getPorcMarca()
	{
		return porcMarca;
	}

	/**
	 * @param porcMarca
	 *           the porcMarca to set
	 */
	public void setPorcMarca(final String porcMarca)
	{
		this.porcMarca = porcMarca;
	}

	/**
	 * @return the valorExencion
	 */
	public String getValorExencion()
	{
		return valorExencion;
	}

	/**
	 * @param valorExencion
	 *           the valorExencion to set
	 */
	public void setValorExencion(final String valorExencion)
	{
		this.valorExencion = valorExencion;
	}

	/**
	 * @return the conservacionHistorica
	 */
	public String getConservacionHistorica()
	{
		return conservacionHistorica;
	}

	/**
	 * @param conservacionHistorica
	 *           the conservacionHistorica to set
	 */
	public void setConservacionHistorica(final String conservacionHistorica)
	{
		this.conservacionHistorica = conservacionHistorica;
	}




}
