package de.hybris.sdh.core.pojos.responses;

/**
 * @author Praxis
 *
 */
public class Cabecera
{
	private String period_key;
	private String nombre_completo;
	private String type;
	private String id_number;
	private String abrzu;
	private String abrzo;
	private String total_retenciones;
	private String total_base_retencion;
	/**
	 * @return the period_key
	 */
	public String getPeriod_key()
	{
		return period_key;
	}
	/**
	 * @param period_key the period_key to set
	 */
	public void setPeriod_key(final String period_key)
	{
		this.period_key = period_key;
	}
	/**
	 * @return the nombre_completo
	 */
	public String getNombre_completo()
	{
		return nombre_completo;
	}
	/**
	 * @param nombre_completo the nombre_completo to set
	 */
	public void setNombre_completo(final String nombre_completo)
	{
		this.nombre_completo = nombre_completo;
	}
	/**
	 * @return the type
	 */
	public String getType()
	{
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(final String type)
	{
		this.type = type;
	}
	/**
	 * @return the id_number
	 */
	public String getId_number()
	{
		return id_number;
	}
	/**
	 * @param id_number the id_number to set
	 */
	public void setId_number(final String id_number)
	{
		this.id_number = id_number;
	}
	/**
	 * @return the abrzu
	 */
	public String getAbrzu()
	{
		return abrzu;
	}
	/**
	 * @param abrzu the abrzu to set
	 */
	public void setAbrzu(final String abrzu)
	{
		this.abrzu = abrzu;
	}
	/**
	 * @return the abrzo
	 */
	public String getAbrzo()
	{
		return abrzo;
	}
	/**
	 * @param abrzo the abrzo to set
	 */
	public void setAbrzo(final String abrzo)
	{
		this.abrzo = abrzo;
	}
	/**
	 * @return the total_retenciones
	 */
	public String getTotal_retenciones()
	{
		return total_retenciones;
	}
	/**
	 * @param total_retenciones the total_retenciones to set
	 */
	public void setTotal_retenciones(final String total_retenciones)
	{
		this.total_retenciones = total_retenciones;
	}
	/**
	 * @return the total_base_retencion
	 */
	public String getTotal_base_retencion()
	{
		return total_base_retencion;
	}
	/**
	 * @param total_base_retencion the total_base_retencion to set
	 */
	public void setTotal_base_retencion(final String total_base_retencion)
	{
		this.total_base_retencion = total_base_retencion;
	}



}
