package de.hybris.sdh.core.pojos.responses;

/**
 * @author Consultor
 *
 */
public class DetalleActivEconomicas
{
	private String activPrincipal;
	private String ciiu;
	private String denominacion;
	private String fechaIniAct;
	private String fechaCeseAct;

	/**
	 * @return the activPrincipal
	 */
	public String getActivPrincipal()
	{
		return activPrincipal;
	}

	/**
	 * @param activPrincipal
	 *           the activPrincipal to set
	 */
	public void setActivPrincipal(final String activPrincipal)
	{
		this.activPrincipal = activPrincipal;
	}

	/**
	 * @return the ciiu
	 */
	public String getCiiu()
	{
		return ciiu;
	}

	/**
	 * @param ciiu
	 *           the ciiu to set
	 */
	public void setCiiu(final String ciiu)
	{
		this.ciiu = ciiu;
	}

	/**
	 * @return the denominacion
	 */
	public String getDenominacion()
	{
		return denominacion;
	}

	/**
	 * @param denominacion
	 *           the denominacion to set
	 */
	public void setDenominacion(final String denominacion)
	{
		this.denominacion = denominacion;
	}

	/**
	 * @return the fechaIniAct
	 */
	public String getFechaIniAct()
	{
		return fechaIniAct;
	}

	/**
	 * @param fechaIniAct
	 *           the fechaIniAct to set
	 */
	public void setFechaIniAct(final String fechaIniAct)
	{
		this.fechaIniAct = fechaIniAct;
	}

	/**
	 * @return the fechaCeseAct
	 */
	public String getFechaCeseAct()
	{
		return fechaCeseAct;
	}

	/**
	 * @param fechaCeseAct
	 *           the fechaCeseAct to set
	 */
	public void setFechaCeseAct(final String fechaCeseAct)
	{
		this.fechaCeseAct = fechaCeseAct;
	}



}
