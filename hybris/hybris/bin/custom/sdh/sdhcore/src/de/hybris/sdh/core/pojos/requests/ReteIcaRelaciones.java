package de.hybris.sdh.core.pojos.requests;

/**
 * @author hybris
 *
 */
public class ReteIcaRelaciones
{
	private String tipoDoc;

	private String numDoc;

	private String nombre;

	private String tipoRelacion;

	private String fechaDesde;

	private String fechaHasta;

	/**
	 * @return the tipoDoc
	 */
	public String getTipoDoc()
	{
		return tipoDoc;
	}

	/**
	 * @param tipoDoc
	 *           the tipoDoc to set
	 */
	public void setTipoDoc(final String tipoDoc)
	{
		this.tipoDoc = tipoDoc;
	}

	/**
	 * @return the numDoc
	 */
	public String getNumDoc()
	{
		return numDoc;
	}

	/**
	 * @param numDoc
	 *           the numDoc to set
	 */
	public void setNumDoc(final String numDoc)
	{
		this.numDoc = numDoc;
	}

	/**
	 * @return the nombre
	 */
	public String getNombre()
	{
		return nombre;
	}

	/**
	 * @param nombre
	 *           the nombre to set
	 */
	public void setNombre(final String nombre)
	{
		this.nombre = nombre;
	}

	/**
	 * @return the tipoRelacion
	 */
	public String getTipoRelacion()
	{
		return tipoRelacion;
	}

	/**
	 * @param tipoRelacion
	 *           the tipoRelacion to set
	 */
	public void setTipoRelacion(final String tipoRelacion)
	{
		this.tipoRelacion = tipoRelacion;
	}

	/**
	 * @return the fechaDesde
	 */
	public String getFechaDesde()
	{
		return fechaDesde;
	}

	/**
	 * @param fechaDesde
	 *           the fechaDesde to set
	 */
	public void setFechaDesde(final String fechaDesde)
	{
		this.fechaDesde = fechaDesde;
	}

	/**
	 * @return the fechaHasta
	 */
	public String getFechaHasta()
	{
		return fechaHasta;
	}

	/**
	 * @param fechaHasta
	 *           the fechaHasta to set
	 */
	public void setFechaHasta(final String fechaHasta)
	{
		this.fechaHasta = fechaHasta;
	}



}
