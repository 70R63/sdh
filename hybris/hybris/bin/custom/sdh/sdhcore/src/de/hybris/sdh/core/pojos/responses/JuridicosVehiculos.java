package de.hybris.sdh.core.pojos.responses;

/**
 * @author hybris
 *
 */
public class JuridicosVehiculos
{
	private String tipoID;
	private String nombre;
	private String numID;
	private String calidad;
	private String procProp;
	private String fechaDesde;
	private String fechaHasta;
	/**
	 * @return the tipoID
	 */
	public String getTipoID()
	{
		return tipoID;
	}
	
	/**
	 * @param tipoID
	 *           the tipoID to set
	 */
	public void setTipoID(final String tipoID)
	{
		this.tipoID = tipoID;
	}
	/**
	 * @return the nombre
	 */
	public String getNombre()
	{
		return nombre;
	}
	
	/**
	 * @param nombre
	 *           the nombre to set
	 */
	public void setNombre(final String nombre)
	{
		this.nombre = nombre;
	}
	/**
	 * @return the numID
	 */
	public String getNumID()
	{
		return numID;
	}
	
	/**
	 * @param numID
	 *           the numID to set
	 */
	public void setNumID(final String numID)
	{
		this.numID = numID;
	}
	/**
	 * @return the calidad
	 */
	public String getCalidad()
	{
		return calidad;
	}
	
	/**
	 * @param calidad
	 *           the calidad to set
	 */
	public void setCalidad(final String calidad)
	{
		this.calidad = calidad;
	}
	/**
	 * @return the procProp
	 */
	public String getProcProp()
	{
		return procProp;
	}
	
	/**
	 * @param procProp
	 *           the procProp to set
	 */
	public void setProcProp(final String procProp)
	{
		this.procProp = procProp;
	}
	/**
	 * @return the fechaDesde
	 */
	public String getFechaDesde()
	{
		return fechaDesde;
	}
	
	/**
	 * @param fechaDesde
	 *           the fechaDesde to set
	 */
	public void setFechaDesde(final String fechaDesde)
	{
		this.fechaDesde = fechaDesde;
	}
	/**
	 * @return the fechaHasta
	 */
	public String getFechaHasta()
	{
		return fechaHasta;
	}
	
	/**
	 * @param fechaHasta
	 *           the fechaHasta to set
	 */
	public void setFechaHasta(final String fechaHasta)
	{
		this.fechaHasta = fechaHasta;
	}



}
