public class TramitesConsultaCasoInfo
{
	private String num_caso;
	private String tipoId;
	private String numDoc;
	/**
	 * @return the num_caso
	 */
	public String getNum_caso()
	{
		return num_caso;
	}
	
	/**
	 * @param num_caso
	 *           the num_caso to set
	 */
	public void setNum_caso(final String num_caso)
	{
		this.num_caso = num_caso;
	}

	/**
	 * @return the tipoId
	 */
	public String getTipoId()
	{
		return tipoId;
	}

	/**
	 * @param tipoId
	 *           the tipoId to set
	 */
	public void setTipoId(final String tipoId)
	{
		this.tipoId = tipoId;
	}

	/**
	 * @return the numDoc
	 */
	public String getNumDoc()
	{
		return numDoc;
	}

	/**
	 * @param numDoc
	 *           the numDoc to set
	 */
	public void setNumDoc(final String numDoc)
	{
		this.numDoc = numDoc;
	}





}
