package de.hybris.sdh.storefront.forms;

/**
 * @author Federico Flores Dimas
 *
 */
public class ForgottenMailForm
{

	private String docNumber;
	private String docype;
	private String mail;
	
	/**
	 * @return the docNumber
	 */
	public String getDocNumber()
	{
		return docNumber;
	}
	
	/**
	 * @param docNumber
	 *           the docNumber to set
	 */
	public void setDocNumber(final String docNumber)
	{
		this.docNumber = docNumber;
	}
	
	/**
	 * @return the docype
	 */
	public String getDocype()
	{
		return docype;
	}
	
	/**
	 * @param docype
	 *           the docype to set
	 */
	public void setDocype(final String docype)
	{
		this.docype = docype;
	}
	
	/**
	 * @return the mail
	 */
	public String getMail()
	{
		return mail;
	}
	
	/**
	 * @param mail
	 *           the mail to set
	 */
	public void setMail(final String mail)
	{
		this.mail = mail;
	}
}
