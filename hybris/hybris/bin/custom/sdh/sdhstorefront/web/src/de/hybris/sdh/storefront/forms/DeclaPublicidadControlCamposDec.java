public class DeclaPublicidadControlCamposDec
{

	private boolean totalAPagar;
	private boolean informacionDeValla;
	private boolean btnPresentarDec;
	private boolean btnPagarDec;


	/**
	 * @return the totalAPagar
	 */
	public boolean isTotalAPagar()
	{
		return totalAPagar;
	}
	
	/**
	 * @param totalAPagar
	 *           the totalAPagar to set
	 */
	public void setTotalAPagar(final boolean totalAPagar)
	{
		this.totalAPagar = totalAPagar;
	}
	
	/**
	 * @return the informacionDeValla
	 */
	public boolean isInformacionDeValla()
	{
		return informacionDeValla;
	}
	
	/**
	 * @param informacionDeValla
	 *           the informacionDeValla to set
	 */
	public void setInformacionDeValla(final boolean informacionDeValla)
	{
		this.informacionDeValla = informacionDeValla;
	}
	/**
	 * @return the btnPresentarDec
	 */
	public boolean isBtnPresentarDec()
	{
		return btnPresentarDec;
	}
	
	/**
	 * @param btnPresentarDec
	 *           the btnPresentarDec to set
	 */
	public void setBtnPresentarDec(final boolean btnPresentarDec)
	{
		this.btnPresentarDec = btnPresentarDec;
	}
	/**
	 * @return the btnPagarDec
	 */
	public boolean isBtnPagarDec()
	{
		return btnPagarDec;
	}
	
	/**
	 * @param btnPagarDec
	 *           the btnPagarDec to set
	 */
	public void setBtnPagarDec(final boolean btnPagarDec)
	{
		this.btnPagarDec = btnPagarDec;
	}


}
